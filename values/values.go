package main

import "fmt"

func main() {
	fmt.Println("go" + "lang")        // strings
	fmt.Println("1+1 =", 1+1)         // integers
	fmt.Println("7.0/3.0 =", 7.0/3.0) // floats
	fmt.Println(true && false)        // booleans
	fmt.Println(true || false)        // booleans
	fmt.Println(!true)                // booleans
}
